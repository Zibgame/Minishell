#include "../libft.h"

/*
big big free
*/
void	free_array(char	**arr)
{
	int	i;

	if (arr)
	{
		i = 0;
		while (arr[i])
			i++;
		while (i >= 0)
		{
			free(arr[i]);
			i--;
		}
		free(arr);
	}
}
