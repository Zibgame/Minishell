#include "../../libft.h"

int	ft_putptr(void *ptr)
{
	if (!ptr)
	{
		write(1, "(nil)", 5);
		return (5);
	}
	write(1, "0x", 2);
	return (2 + ft_putnbr_hexl((unsigned long)ptr, 0));
}
