#include "../../libft.h"

int	ft_putnbr(int n)
{
	int	i;
	int	j;

	j = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		j = 1;
		if (n == -2147483648)
		{
			write(1, "2147483648", 10);
			return (11);
		}
		else
			n *= -1;
	}
	if (n < 10)
	{
		i = '0' + n;
		write(1, &i, 1);
		return (j + 1);
	}
	else
		return (j + ft_putnbr(n / 10) + ft_putnbr(n % 10));
}
