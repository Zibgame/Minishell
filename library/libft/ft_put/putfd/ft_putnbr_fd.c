#include "../../libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	if (n < 0)
	{
		if (n == -2147483647 -1)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		else
		{
			write(fd, "-", 1);
			n *= -1;
		}
	}
	if (n < 10)
	{
		i = '0' + n;
		write(fd, &i, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
